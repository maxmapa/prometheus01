"""
Функція all() в Python є вбудованою функцією, яка приймає ітерабельний об'єкт (такий як список, кортеж або генераторне вираження) і повертає True, якщо всі елементи цього об'єкта оцінюються як True, та False, якщо хоча б один елемент оцінюється як False.

У контексті, який я використав, all() перевіряє, чи виконується умова для кожного елемента у генераторному виразі. Вираз (n[i] >= n[i-1] for i in range(1, len(n))) створює генератор, який перевіряє, чи кожен елемент у списку n є більшим або рівним залишнім елементам. Функція all() потім перевіряє, чи всі значення цього генератора є True.
"""
n = input("Enter a number: ")

# Check if it's a tall number
is_tall = all(n[i] >= n[i-1] for i in range(1, len(n)))
print(is_tall)